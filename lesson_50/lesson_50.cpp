#include<iostream>
#include<string>
#include<ctime>
using namespace std;

void main()
{
	setlocale(LC_ALL, "ru");
	srand(time(NULL));

	int size = 0;
	cout << "Введите размер массива: ";
	cin >> size;
	int *arr = new int[size];

	for (int i = 0; i < size; i++)
		arr[i] = rand() % 10;

	for (int i = 0; i < size; i++)
		cout << "Индекс ячейки в массиве - " << i << " Данные в ячейке массива - " << arr[i] << " " << " Адрес ячейки в массиве - " << arr + i << endl;

	delete [] arr;
}
